                When it comes to blood transfusions, it's essential to understand the different types of blood and their
                compatibility. Human blood is classified into different types based on the presence or absence of
                certain antigens on the surface of red blood cells. The two most important blood classification systems
                are the ABO system and the Rh system.
            </p>
            <br>
            <h5 class="secondary-headings scolor">The ABO System</h5>

            <p class="white">The ABO system categorizes blood into four main types: A, B, AB, and O. Each blood type has
                specific antigens and antibodies:</p>

            <ul class="white">
                <li> <i class="scolor">Type A: </i> has A antigens on the red blood cells and anti-B antibodies in the
                    plasma.</li>
                <li> <i class="scolor">Type B: </i> has B antigens on the red blood cells and anti-A antibodies in the
                    plasma.</li>
                <li> <i class="scolor">Type AB:</i>i It has both A and B antigens on the red blood cells but neither
                    anti-A nor anti-B antibodies in the plasma.</li>
                <li> <i class="scolor">Type O: </i> has neither A nor B antigens on the red blood cells but both anti-A
                    and anti-B antibodies in the plasma.</li>
            </ul>
            <br>
            <h5 class="secondary-headings scolor">The Rh System</h5>

            <p class="white">The Rh system determines the presence or absence of the Rh antigen on the surface of red
                blood cells. If the antigen is present, the blood type is Rh-positive (Rh+); if the antigen is absent,
                the blood type is Rh-negative (Rh-)</p>
